    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if(username.getText().equals("") && password.getText().equals("")){
             JOptionPane.showMessageDialog(null, "Username and Password Required!!");
        }else{
        try {
            Connection conn = Koneksi.getKoneksi();
            Statement stm = conn.createStatement();
            ResultSet sql = stm.executeQuery("SELECT * FROM tbLogin where username='" + username.getText() 
                    + "' and sandi = '" + password.getText() + "'");

            if (sql.next()) {
                if (password.getText().equals(sql.getString("sandi"))) {
                   
                    JOptionPane.showMessageDialog(null, "Login berhasil");
                    this.dispose();
                    FormMenu fm = new FormMenu();
                    fm.setVisible(true);
                    fm.setLocationRelativeTo(null);
                    this.setVisible(false);
                } else {
                    JOptionPane.showMessageDialog(null, "Username dan Password Salah");
                    username.setText("");
                    password.setText("");
                    username.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Username dan Password Tidak Tersedia");
                username.setText("");
                password.setText("");
                username.requestFocus();
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan!!!");
        }
       }
    }//GEN-LAST:event_btnLoginActionPerformed

